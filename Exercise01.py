RUSSIAN_VOWELS = ['аеёиоуыэюя']

# Функиця анализа ритма стихотврения
def rhythm(phrases):
    sentences = phrases.lower().split(' ')
    funct = lambda x: sum(1 for i in x if i in RUSSIAN_VOWELS)
    temp = funct(sentences[0])
    if all([funct(i) == temp for i in sentences]):
        return True
    return False

print("Программа проверяет, есть ли ритм (одинаковое количство гласных букв) в ведённом стихотворении.")
