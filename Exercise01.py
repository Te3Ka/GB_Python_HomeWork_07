print('Пример: "Хорошо-живёт-на-свете-Винни-Пух Оттого-поёт-он-эти-песни-в-слух И-не-важно-чем-он-занят"')
print("Для анализа ритма стихотворения нужно нажать Enter")
print('Если ритм в стихотворении есть, программа показывает фразу "Парам пам-пам"')
print('Если ритм в стихотворении отсутствует, программа показывает фразу "Пам парам"')

poem = input("Введите стихотврение:\n")
print("Парам пам-пам" if rhythm(poem) else "Пам парам")
